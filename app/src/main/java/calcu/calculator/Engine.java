public class Engine {

    public static double Add (double num1, double num2){
        return num1 + num2;
    }

    public static double Sub (double num1, double num2){
        return num1 - num2;
    }

    public static double Mul (double num1, double num2){
        return num1 * num2;
    }

    public static double Div (double num1, double num2){
        return num1 / num2;
    }

}
